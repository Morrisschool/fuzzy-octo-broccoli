function clear(){
    for (let i = 1; i <= 7; i++) {
        let x = document.getElementById('iframemn' + i);
        x.style.display = "none";
    }
}

function mn1(){
    clear();
    let x = document.getElementById('iframemn1');
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

